-- ============================================================
-- 医疗数据库初始化脚本 (Medical Database for Text2SQL Evaluation)
-- 字符集: UTF8MB4
-- ============================================================

DROP DATABASE IF EXISTS medical_db;
CREATE DATABASE medical_db DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE medical_db;

-- ============================================================
-- 1. 科室表 (Departments)
-- ============================================================
CREATE TABLE departments (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    description TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================
-- 2. 疾病表 (Diseases)
-- ============================================================
CREATE TABLE diseases (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    icd_code VARCHAR(20) NOT NULL,
    description TEXT,
    prevalence DECIMAL(8,4) COMMENT '患病率(每万人)',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_icd_code (icd_code),
    INDEX idx_name (name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================
-- 3. 疾病-科室映射表 (Disease-Department Mapping)
-- ============================================================
CREATE TABLE disease_department_mapping (
    disease_id INT NOT NULL,
    department_id INT NOT NULL,
    PRIMARY KEY (disease_id, department_id),
    FOREIGN KEY (disease_id) REFERENCES diseases(id) ON DELETE CASCADE,
    FOREIGN KEY (department_id) REFERENCES departments(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================
-- 4. 症状表 (Symptoms)
-- ============================================================
CREATE TABLE symptoms (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    description TEXT,
    INDEX idx_name (name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================
-- 5. 疾病-症状关系表 (Disease-Symptom Relationships)
-- ============================================================
CREATE TABLE disease_symptoms (
    disease_id INT NOT NULL,
    symptom_id INT NOT NULL,
    frequency ENUM('common','occasional','rare') NOT NULL DEFAULT 'common' COMMENT '出现频率: common=常见, occasional=偶见, rare=罕见',
    PRIMARY KEY (disease_id, symptom_id),
    FOREIGN KEY (disease_id) REFERENCES diseases(id) ON DELETE CASCADE,
    FOREIGN KEY (symptom_id) REFERENCES symptoms(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================
-- 6. 药品表 (Drugs)
-- ============================================================
CREATE TABLE drugs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    generic_name VARCHAR(200),
    category VARCHAR(100),
    dosage_form VARCHAR(50) COMMENT '剂型',
    price DECIMAL(10,2) COMMENT '单价(元)',
    manufacturer VARCHAR(200),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_name (name),
    INDEX idx_category (category)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================
-- 7. 处方表 (Prescriptions)
-- ============================================================
CREATE TABLE prescriptions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    disease_id INT NOT NULL,
    drug_id INT NOT NULL,
    dosage VARCHAR(100) COMMENT '用量',
    frequency VARCHAR(100) COMMENT '用药频次',
    duration VARCHAR(100) COMMENT '疗程',
    FOREIGN KEY (disease_id) REFERENCES diseases(id) ON DELETE CASCADE,
    FOREIGN KEY (drug_id) REFERENCES drugs(id) ON DELETE CASCADE,
    INDEX idx_disease (disease_id),
    INDEX idx_drug (drug_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================
-- 8. 药物相互作用表 (Drug Interactions)
-- ============================================================
CREATE TABLE drug_interactions (
    drug_id_1 INT NOT NULL,
    drug_id_2 INT NOT NULL,
    severity ENUM('mild','moderate','severe') NOT NULL COMMENT '严重程度: mild=轻度, moderate=中度, severe=重度',
    description TEXT,
    PRIMARY KEY (drug_id_1, drug_id_2),
    FOREIGN KEY (drug_id_1) REFERENCES drugs(id) ON DELETE CASCADE,
    FOREIGN KEY (drug_id_2) REFERENCES drugs(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


